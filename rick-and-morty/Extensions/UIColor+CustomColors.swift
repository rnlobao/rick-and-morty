import UIKit

extension UIColor {
    
    // MARK: - New Colors: customGray
        
    /// Cor: Custom Gray 100% - Hex : #D9D9D9
    /// - Returns: UIColor #D9D9D9.
    static var customGray: UIColor {
        return UIColor(red: 0.85, green: 0.85, blue: 0.85, alpha: 1.0)
    }
}
